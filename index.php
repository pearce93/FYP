							slitslider.next();
							return false;
						});

						$navArrows.children( ':first' ).on( 'click', function() {							
							slitslider.previous();
							return false;
						});

						$nav.each( function( i ) {
							$( this ).on( 'click', function( event ) {
								
								var $dot = $( this );
								
